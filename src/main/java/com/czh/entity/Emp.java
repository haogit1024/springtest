package com.czh.entity;

public class Emp{
	private Integer empId;
	private Integer deptId;
	private String name;
	private String job;
	private Integer tel;
	private String mob;
	private String remark;
	private Integer roleId;
	private String binding;
	private String openId;

	public Integer getEmpId() { return empId; } 

	public void setEmpId(Integer empId) { this.empId=empId; } 

	public Integer getDeptId() { return deptId; } 

	public void setDeptId(Integer deptId) { this.deptId=deptId; } 

	public String getName() { return name; } 

	public void setName(String name) { this.name=name; } 

	public String getJob() { return job; } 

	public void setJob(String job) { this.job=job; } 

	public Integer getTel() { return tel; } 

	public void setTel(Integer tel) { this.tel=tel; } 

	public String getMob() { return mob; } 

	public void setMob(String mob) { this.mob=mob; } 

	public String getRemark() { return remark; } 

	public void setRemark(String remark) { this.remark=remark; } 

	public Integer getRoleId() { return roleId; } 

	public void setRoleId(Integer roleId) { this.roleId=roleId; } 

	public String getBinding() { return binding; } 

	public void setBinding(String binding) { this.binding=binding; } 

	public String getOpenId() { return openId; } 

	public void setOpenId(String openId) { this.openId=openId; } 

}